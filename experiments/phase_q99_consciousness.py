sys.path.insert(0, os.path.dirname(__file__))
from utils import load_model


def compute_phi(hidden_states):
    """Compute a simplified Integrated Information (Phi) metric.
    Phi measures how much information is generated by the whole system
    beyond what is generated by its parts."""
    hs = hidden_states.astype(np.float32)
    seq_len, d_model = hs.shape

    if seq_len < 2:
        return 0.0

    # Full system mutual information (using covariance)
    try:
        cov_full = np.cov(hs.T)
        eigvals_full = np.linalg.eigvalsh(cov_full)
        eigvals_full = eigvals_full[eigvals_full > 1e-10]
        entropy_full = np.sum(np.log(eigvals_full + 1e-10))
    except:
        return 0.0

    # Split into two halves and compute sum of parts
    mid = seq_len // 2
    part1 = hs[:mid]
    part2 = hs[mid:]

    try:
        cov1 = np.cov(part1.T)
        eigvals1 = np.linalg.eigvalsh(cov1)
        eigvals1 = eigvals1[eigvals1 > 1e-10]
        entropy1 = np.sum(np.log(eigvals1 + 1e-10))

        cov2 = np.cov(part2.T)
        eigvals2 = np.linalg.eigvalsh(cov2)
        eigvals2 = eigvals2[eigvals2 > 1e-10]
        entropy2 = np.sum(np.log(eigvals2 + 1e-10))
    except:
        return 0.0

    # Phi = entropy_full - (entropy_part1 + entropy_part2)
    # Positive Phi means the whole is more than sum of parts
    phi = entropy_full - (entropy1 + entropy2)
    return float(phi)


def measure_phi_across_layers(model, tokenizer, num_layers):
    """Measure Phi at each layer."""
    prompts = [
        "I think therefore I am and my consciousness emerges from",
        "The binding problem asks how separate neural processes create unified",
        "Awareness arises when information is integrated across the entire",
    ]

    phi_by_layer = {}

    for layer_idx in range(0, num_layers, max(1, num_layers // 14)):
        layer_phis = []
        for prompt in prompts:
            inputs = tokenizer(prompt, return_tensors='pt').to(model.device)
            captured = [None]

            def capture_hook(module, args, output, store=captured):
                if isinstance(output, tuple):
                    store[0] = output[0][0].detach().cpu().float().numpy()
                else:
                    store[0] = output.detach().cpu().float().numpy()
                    if store[0].ndim == 3:
                        store[0] = store[0][0]

            handle = model.model.layers[layer_idx].register_forward_hook(capture_hook)
            with torch.no_grad():
                model(**inputs)
            handle.remove()

            if captured[0] is not None:
                phi = compute_phi(captured[0])
                layer_phis.append(phi)

        if layer_phis:
            phi_by_layer[layer_idx] = {
                'mean_phi': float(np.mean(layer_phis)),
                'std_phi': float(np.std(layer_phis)),
            }

    return phi_by_layer


def main():
    print("=" * 60)
    print("Phase Q99: Consciousness & Integrated Information (Phi)")
    print("=" * 60)
    t0 = time.time()

    model, tokenizer = load_model()
    num_layers = model.config.num_hidden_layers

    print("  Computing Integrated Information (Phi)...")
    phi_data = measure_phi_across_layers(model, tokenizer, num_layers)

    for layer, data in sorted(phi_data.items()):
        print("    Layer %d: Phi=%.4f" % (layer, data['mean_phi']))

    # Analysis
    phis = [data['mean_phi'] for data in phi_data.values()]
    max_phi = max(phis) if phis else 0
    max_phi_layer = max(phi_data, key=lambda l: phi_data[l]['mean_phi']) if phi_data else 0

    fig, axes = plt.subplots(1, 2, figsize=(12, 5))

    ax = axes[0]
    layers = sorted(phi_data.keys())
    phi_vals = [phi_data[l]['mean_phi'] for l in layers]
    phi_stds = [phi_data[l]['std_phi'] for l in layers]
    ax.plot(layers, phi_vals, 'o-', color='#9C27B0', linewidth=2.5, markersize=8)
    ax.fill_between(layers,
                    [v - s for v, s in zip(phi_vals, phi_stds)],
                    [v + s for v, s in zip(phi_vals, phi_stds)],
                    alpha=0.15, color='#9C27B0')
    ax.set_xlabel('Layer index', fontsize=12)
    ax.set_ylabel('Integrated Information (Phi)', fontsize=12)
    ax.set_title('(a) Phi Across Layers\nPeak at Layer %d' % max_phi_layer,
                 fontsize=12, fontweight='bold')
    ax.grid(alpha=0.3)

    ax = axes[1]
    consciousness_level = 'HIGH' if max_phi > 100 else 'MODERATE' if max_phi > 10 else 'LOW'
    color = '#4CAF50' if max_phi > 100 else '#FF9800' if max_phi > 10 else '#F44336'
    ax.text(0.5, 0.6,
            'Proto-Consciousness\nLevel: %s' % consciousness_level,
            ha='center', va='center', fontsize=16, fontweight='bold',
            color=color, transform=ax.transAxes)
    ax.text(0.5, 0.3,
            'Max Phi = %.1f\nPeak at Layer %d/%d\n\n'
            'The whole > sum of parts' % (max_phi, max_phi_layer, num_layers),
            ha='center', va='center', fontsize=11, transform=ax.transAxes)
    ax.axis('off')
    ax.set_title('(b) IIT Classification', fontsize=12, fontweight='bold')

    plt.suptitle('Integrated Information Theory: Proto-Consciousness in S-Qubits',
                 fontsize=13, fontweight='bold', y=1.02)
    plt.tight_layout()
    fig_path = os.path.join(FIGURES_DIR, 'phase_q99_consciousness.png')
    plt.savefig(fig_path, dpi=150, bbox_inches='tight')
    plt.close()

    del model, tokenizer
    gc.collect()
    torch.cuda.empty_cache()

    elapsed = time.time() - t0
    results = {
        'phase': 'Q99', 'name': 'Integrated Information (Phi)',
        'max_phi': float(max_phi),
        'max_phi_layer': int(max_phi_layer),
        'consciousness_level': consciousness_level,
        'phi_data': {str(k): v for k, v in phi_data.items()},
        'elapsed': elapsed,
    }
    res_path = os.path.join(RESULTS_DIR, 'phase_q99_consciousness.json')
    with open(res_path, 'w') as f:
        json.dump(results, f, indent=2, default=str)
    print("  Max Phi: %.1f at layer %d. Elapsed: %.1fs" %
          (max_phi, max_phi_layer, elapsed))
    return results


if __name__ == '__main__':
    main()
